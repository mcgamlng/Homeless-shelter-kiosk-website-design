const separatorWords = {
  es: "y",
  hmn: "thiab",
  so: "iyo"
};

export function normalizeActivityName(value) {
  return String(value || "")
    .trim()
    .replace(/\s+/g, " ")
    .toLowerCase();
}

export function getSavedActivityTranslation(activityOrName, language = "en") {
  if (language === "en" || typeof activityOrName === "string") return "";
  const fieldValue =
    activityOrName?.[`name_${language}`] ||
    activityOrName?.[`activity_name_${language}`] ||
    activityOrName?.translations?.[language] ||
    activityOrName?.translated_names?.[language];
  return cleanTranslation(fieldValue);
}

export function translateActivityName(activityOrName, language = "en") {
  const name =
    typeof activityOrName === "string"
      ? activityOrName
      : activityOrName?.name || activityOrName?.activity_name;
  const text = cleanTranslation(name);
  if (!text || language === "en") return text;

  const savedTranslation = getSavedActivityTranslation(activityOrName, language);
  if (savedTranslation) return savedTranslation;

  return generateActivityTranslation(text, language) || text;
}

export function buildActivityTranslations(name) {
  return ACTIVITY_TRANSLATION_LANGUAGES.reduce((translations, language) => {
    translations[`name_${language}`] = generateActivityTranslation(name, language);
    return translations;
  }, {});
}

export function buildScheduledActivityTranslations(activity) {
  return ACTIVITY_TRANSLATION_LANGUAGES.reduce((translations, language) => {
    translations[`activity_name_${language}`] =
      cleanTranslation(activity?.[`name_${language}`]) ||
      generateActivityTranslation(activity?.name || activity?.activity_name, language);
    return translations;
  }, {});
}

function generateActivityTranslation(name, language = "en") {
  const text = cleanTranslation(name);
  if (!text || language === "en") return text;

  const exact = exactTranslations[language]?.[normalizeActivityName(text)];
  if (exact) return exact;

  const phraseTranslation = translateCompoundPhrase(text, language);
  if (phraseTranslation) return phraseTranslation;

  const wordTranslation = translateByWord(text, language);
  return wordTranslation || "";
}

function translateCompoundPhrase(text, language) {
  const pieces = text.split(/(\s*\/\s*|\s*&\s*|\s+\+\s+|\s+and\s+|\s*,\s*)/i);
  let translatedPieces = 0;
  const translated = pieces
    .map((piece) => {
      const cleanPiece = piece.trim();
      if (!cleanPiece) return "";
      if (isSeparator(cleanPiece)) return translateSeparator(cleanPiece, language);
      const exact = exactTranslations[language]?.[normalizeActivityName(cleanPiece)];
      if (exact) {
        translatedPieces += 1;
        return exact;
      }
      const words = translateByWord(cleanPiece, language);
      if (words && words !== cleanPiece) {
        translatedPieces += 1;
        return words;
      }
      return cleanPiece;
    })
    .join("")
    .replace(/\s+([,/])/g, "$1")
    .replace(/([,/])(?=\S)/g, "$1 ")
    .replace(/\s{2,}/g, " ")
    .trim();

  return translatedPieces > 0 ? translated : "";
}

function translateByWord(text, language) {
  const dictionary = wordTranslations[language];
  if (!dictionary) return "";
  const tokens = text.match(/[A-Za-z0-9-]+|[^A-Za-z0-9-]+/g) || [];
  let translatedWords = 0;
  const translated = tokens
    .map((token) => {
      if (!/[A-Za-z0-9]/.test(token)) return token;
      const lowerToken = token.toLowerCase();
      if (dictionary[lowerToken]) {
        translatedWords += 1;
        return dictionary[lowerToken];
      }
      return token;
    })
    .join("")
    .replace(/\s{2,}/g, " ")
    .trim();

  if (translatedWords === 0) return "";
  return uppercaseFirst(translated);
}

function isSeparator(value) {
  return /^\/|&|\+|,|and$/i.test(value);
}

function translateSeparator(value, language) {
  if (value === "/" || value === "," || value === "+") return ` ${value} `;
  if (value === "&" || /^and$/i.test(value)) return ` ${separatorWords[language] || "and"} `;
  return ` ${value} `;
}

function cleanTranslation(value) {
  return String(value || "")
    .trim()
    .replace(/\s+/g, " ");
}

function uppercaseFirst(value) {
  return value ? `${value[0].toUpperCase()}${value.slice(1)}` : value;
}
