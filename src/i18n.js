function normalizeActivityName(value) {
  return String(value || "")
    .trim()
    .replace(/\s+/g, " ")
    .toLowerCase();
}

export function translateActivityName(activityOrName, language = "en") {
  return translateStoredActivityName(activityOrName, language);
}

export function translateLegacyActivityName(activityOrName, language = "en") {
  const name =
    typeof activityOrName === "string"
      ? activityOrName
      : activityOrName?.name || activityOrName?.activity_name;
  const normalizedName = normalizeActivityName(name);
  const exactTranslation = activityTranslations[language]?.[normalizedName];
  if (exactTranslation) return exactTranslation;
  return translateCustomActivityName(name, language);
}

function translateCustomActivityName(name, language = "en") {
  const text = String(name || "").trim();
  const translationsForLanguage = activityTranslations[language];
  if (!text || !translationsForLanguage) return text;

  const pieces = text.split(/(\s*\/\s*|\s*&\s*|\s+\+\s+|\s+and\s+|\s*,\s*)/i);
  let translatedPieces = 0;
  let untranslatedPieces = 0;
  const translated = pieces
    .map((piece) => {
      const cleanPiece = piece.trim();
      if (!cleanPiece) return "";
      if (isActivitySeparator(cleanPiece)) return translatedActivitySeparator(cleanPiece, language);
      const exactPiece = translationsForLanguage[normalizeActivityName(cleanPiece)];
      if (exactPiece) {
        translatedPieces += 1;
        return exactPiece;
      }
      untranslatedPieces += 1;
      return cleanPiece;
    })
    .join("")
    .replace(/\s+([,/])/g, "$1")
    .replace(/([,/])(?=\S)/g, "$1 ")
    .replace(/\s{2,}/g, " ")
    .trim();

  return translatedPieces > 0 && untranslatedPieces === 0 ? translated : text;
}

function isActivitySeparator(value) {
  return /^\/|&|\+|,|and$/i.test(value);
}

function translatedActivitySeparator(value, language) {
  if (value === "/" || value === "," || value === "+") return ` ${value} `;
  if (value === "&" || /^and$/i.test(value)) {
    const words = { es: "y", hmn: "thiab", so: "iyo" };
    return ` ${words[language] || "and"} `;
  }
  return ` ${value} `;
}

export function formatActivityDuration(minutes, language = "en") {
  return `${Number(minutes)} ${translations[language]?.minuteLabel || translations.en.minuteLabel}`;
}

export function formatActivityDurationForSpeech(minutes, language = "en") {
  const cleanMinutes = Number(minutes);
  const labels = {
    en: cleanMinutes === 1 ? "minute" : "minutes",
    es: cleanMinutes === 1 ? "minuto" : "minutos",
    hmn: "feeb",
    so: "daqiiqo"
  };
  return `${cleanMinutes} ${labels[language] || labels.en}`;
}

export function formatActivityClock(value, language = "en") {
  const [hours, minutes] = String(value || "")
    .split(":")
    .map(Number);
  if (!Number.isFinite(hours) || !Number.isFinite(minutes)) return String(value || "");
  const date = new Date(2000, 0, 1, hours, minutes);
  const locales = { en: "en-US", es: "es-US", hmn: "hmn", so: "so-SO" };
  return new Intl.DateTimeFormat(locales[language] || locales.en, {
    hour: "numeric",
    minute: "2-digit"
  }).format(date);
}
